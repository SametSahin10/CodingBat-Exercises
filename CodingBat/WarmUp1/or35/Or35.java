package or35;

/*
*
* Return true if the given non-negative number is a multiple of 3 
* or a multiple of 5. Use the % "mod" operator.
*
*/


/**
 *
 * @author sametsahin
 */

public class Or35 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
    }
    
    public boolean or35(int n){
        
        return n % 3 == 0 || n % 5 == 0;
        
    }
    
}
